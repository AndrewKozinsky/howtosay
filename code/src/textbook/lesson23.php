                    <p>ПЛФ <a href="lesson22.php">предыдущего урока</a> «Я сделаю это завтра», например «Я пойду завтра в кино», мы говорим в случае, когда принимаем решение, что что-то должно произойти в будущем. Но уверенности в этом нет. Употребляя будущее грамматическое время мы подчеркиваем лишь вероятность этого.</p>
                    <div class="separately-block6"></div>
                    <p>Но в момент разговора мы можем быть абсолютно уверены, что событие обязательно произойдет в будущем. Например фразу «Я завтра иду в кино» мы говорим если еще до начала разговора:</p>
                    <div class="example-w">
                        <ol>
                            <li><p>Приняли решение о том, что должно произойти;</p></li>
                            <li><p>Заранее договорились о том, что должно произойти;</p></li>
                            <li><p>Предприняли всё для того, чтобы это произошло (купили билеты).</p></li>
                        </ol>
