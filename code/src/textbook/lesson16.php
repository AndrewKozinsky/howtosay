                    <p><b>Дух времени</b> — наличие результата к настоящему моменту.</p>
                    <table>
                        <tr>
                            <td>
                                <p class="pr10">Слова-показатели:</p>
                            </td>
                            <td>
                                <p>уже — <b>already</b></p>
                                <p>только что — <b>just</b></p>
                            </td>
                        </tr>
                    </table>
                    <div class="separately-block4"></div>

                    <p>Формула времени — <b>have + v-ed</b> или <b>have + v3</b>. Если глагол правильный, то добавляется приставка <b>-ed</b> как и в Past Simple. Если неправильный, то берется третья форма глагола. Она указана в <a href="../wrong-verb.php">таблице неправильных глаголов</a>.</p>
                    <div class="example-w">
                        <p>Я уже посмотрел телевизор. — <b>I have already watched TV.</b></p>
                        <p>Я только что посмотрел телевизор. — <b>I have just watched TV.</b></p>
                        <p>Он уже написал письмо. — <b>He has already written a letter.</b></p>
                        <p>Она только что пришла. — <b>She has just come.</b></p>
                        <p>Мы уже пообедали. — <b>We have already had dinner.</b></p>
                    </div>


                    <div class="separately-block3"></div>

                    <div class="advice-w">
                        <div class="advice-block-name">Совет</div>
                        <p>He has и he is сокращенно пишется как he's. Но после he has пишется глагол — He's invited me to dinner tonight. А после he is существительное или прилагательное — He's late.</p>
                    </div>


                    <div class="separately-block3"></div>

                    <p><b>Вопросительная форма</b> образуется путем постановки глагола <b>have (has)</b> перед подлежащим.</p>
                    <div class="example-w">
                        <p>Она только что пришла? — <b>Has she just come?</b></p>
                    </div>
                    <div class="separately-block6"></div>
                    <p>Обратите внимание, что последний ударный слог вопросительного предложения произносится с повышением тона.</p>
                    <div class="separately-block3"></div>

                    <p><b>Отрицательная форма</b> образуется путем постановки отрицания <b>not</b> (которое произносится с ударением) после вспомогательного глагола <b>have(has)</b>.</p>
                </div>


                <div class="separately-block3"></div>

                <div class="advice-w">
                    <div class="advice-block-name">Совет</div>
                    <p>Как вы помните если глагол не употребляется в группе Progressive (см. <a href="lesson8.php">урок 8</a>), а нужно построить предложение в Perfect Progressive, то используют группу времен Perfect.</p>
                </div>

                <div class="separately-block3"></div>

                <div class="advice-w">
                    <div class="advice-block-name">Совет</div>
                    <p>Запоминайте как строятся грамматические времена и в каких случаях применяются. Эти знания понадобятся не только для выполнения упражнений, но и в главах посвященных пассивному залогу.</p>
                </div>


