                        <h3>Грамматический нюанс 6</h3>
                        <p>В английском языке слову «предлагать» соответствуют сразу три глагола: <b>to offer</b>, <b>to suggest</b>, <b>to propose</b>, однако они не являются полными синонимами, то есть их значения не совсем совпадают.</p>
                        <div class="example-w">
                            <p>Употребляя глагол <b>to offer</b>, мы:</p>
                            <div class="example-w">
                                <ol>
                                    <li>
                                        <p>Предлагаем то, чем мы сами располагаем («Я могу предложить вам работу, чашку кофе» – I can offer you a job, a cup of coffee).</p>
                                    </li>
                                    <li>
                                        <p>Предлагаем сделать что-либо, причем делать это будем мы сами («Я предлагаю вам помочь» – I offer to help you).</p>
                                    </li>
                                </ol>
                                <div class="separately-block6"></div>
                            </div>
                            <p>Употребляя глагол <b>to suggest</b>, мы:</p>
                            <div class="example-w">
                                <ol>
                                    <li>
                                        <p>предлагаем какой-то план, в реализации которого будут принимать участие и тот, кто предложил что-то сделать, и тот, кому предложили что-то сделать – Я предлагаю пойти в кино</p>
                                    </li>
                                    <li>
                                        <p>предлагаем какой-то план, в реализации которого тот, кто предложил что-то сделать, может и не принимать участие – «Я предлагаю ему пойти в кино». После глагола to suggest нельзя использовать инфинитив. Вместо него используется –</p>
                                        <div class="example-w">
                                            <ol>
                                                <li>
                                                    <p>герундий – основа глагола с окончанием V-ing<br /><b>I suggest going to the cinema.</b></p>
                                                </li>
                                                <li>
                                                    <p>особая конструкция с вспомогательным глаголом should и словом that<br /><b>I suggest that he should go to the cinema.</b></p>
                                                </li>
                                            </ol>
                                        </div>
                                    </li>
                                </ol>
                                <div class="separately-block6"></div>
                            </div>
                            <p>Употребляя глагол <b>to propose</b>, мы:</p>
                            <ol>
                                <li>
                                    <p>вносим какое-то предложение, например, называем кандидатуру на какую-то должность – «Я предлагаю Сидорова» – I propose Sidorov.</p>
                                </li>
                                <li>
                                    <p>делаем кому-то предложение о браке, то есть предлагаем руку и сердце – «Я делаю тебе предложение» – <b>I propose to you</b>. В разговорной речи мы просто скажем: <b>Marry me</b> – «Выходи за меня».</p>
                                    <p>Если с глаголом <b>to offer</b> возможны оба варианта пассивной конструкции (см. грамматический нюанс 1), то с глаголами <b>to suggest</b> и <b>to propose</b> возможен только один вариант.</p>
                                    <div class="example-w">
                                        <p>Предлагать — <b>to suggest</b></p>
                                        <div class="example-w">
                                            <p>Я предлагаю пойти в кино. — <b>I suggest going to the cinema</b></p>
                                            <p>Мне предлагают пойти в кино. — <b>It is suggested to me going to the cinema</b></p>
                                        </div>
                                        <div class="separately-block6"></div>
                                        <p>Предлагать — <b>to propose</b></p>
                                        <div class="example-w">
                                            <p>Я предлагаю вам руку и сердце. — <b>I propose to you</b></p>
                                            <p>Мне часто предлагают руку и сердце. — <b>It is often proposed to me</b></p>
                                        </div>
                                        <div class="separately-block6"></div>
                                        <p>Предлагать — <b>to offer</b></p>
                                        <div class="example-w">
                                            <p>Я предлагаю работу. — <b>I offer a job</b></p>
                                            <p>Мне предлагают работу. — <b></b></p>
