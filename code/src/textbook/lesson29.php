                        <div class="t">
                            <h3>Грамматический нюанс 10</h3>
                            <p>ПЛФ «Он сказал, что сделает это завтра, если погода будет хорошая» — это сложноподчиненное предложение с придаточным дополнительным, которое имеет при себе зависимое предложение — придаточное условия:</p>
                            <div class="example-w">
                                <p>Он сказал (<i>что?</i>), что сделает это (<i>при каком условии?</i>), если погода будет хорошая.</p>
                            </div>
                            <div class="separately-block4"></div>
                            <p>Правило согласования действует в обоих придаточных, но не забывайте, что при переводе на английский язык <b>придаточного условия</b> надо применять последовательно два правила:</p>
                            <div class="example-w">
                                <ol>
                                    <li>
                                        <p>Употребление настоящих грамматических времен в значении будущего в придаточных времени и условия (<a href="lesson25.php">урок 25</a>).</p>
                                    </li>
                                    <li>
                                        <p>Правило согласования («шаг назад»).</p>
                                        <div class="separately-block6"></div>
                                        <div class="example-w">
                                            <p>Он сказал, что сделает это если погода будет хорошая. — <b>He said that he would do it if the weather was good.</b></p>

                                            <p>Он сказал, что сделает это до того, как пойдет дождь. — <b>He said he would have done it before it started raining.</b></p>
                                            <p>Он думал, что сделает это, когда вырастет. — <b>He thought he would do it when he grew up.</b></p>
                                            <p>Он пообещал, что сделает это, если не будет очень занят. — <b>He promised he would do it if he wasn’t too busy.</b></p>
                                        </div>
                                    </li>
                                </ol>
                            </div>
