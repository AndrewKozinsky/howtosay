                    <div class="advice-w">
                        <div class="advice-block-name">Совет</div>
                        <p>Для русскоговорящего человека может показаться странным изменение слова в придаточном предложении. Например <i>tomorrow</i> на <i>the next day</i>. Всё дело в том, что у англичан другой способ интерпретации речи.</p>
                        <p>Если русскоязычному человеку сказать фразу «1 сентября он сказал, что сделает это завтра» то она будет восприниматься двояко. «Завтра» — это 2 сентября или «завтра» отсчитывая от сегодняшнего дня?</p>
                        <p>А англичанин аналогичной фразой <i>First September he said he would do it the next day</i> четко обозначает, что дело будет сделано 2-го сентября. А если сказать <i>First September he said he would do it tomorrow</i>, то дело будет сделано завтра начиная с текущего момента. Если сегодня 11 сентября, то 12 сентября. Поэтому такие фразы переводятся на русский одинаково, но имеют разные смыслы.</p>
                        <p>По причине другого способа интерперетации речи англичанами и нужно делать «шаг назад» когда мы сообщаем, что кто-то сказал, что делает это сейчас. Он делал это в то, прошедшее время. Hоэтому на это и делается поправка. Поэтому они и говорят «Он сказал, что делал это тогда».</p>
