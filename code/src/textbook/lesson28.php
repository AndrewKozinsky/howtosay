                    <p>Для образования вопросительного предложения вспомогательный глагол <b>will</b> ставят перед подлежащим. При отрицании после <b>will</b> ставят <b>not</b>.</p>
                    <div class="example-w">
                        <p>Мы будем лететь безостановочно в течение 15 часов прежде, чем доберемся до места назначения. — <b>We’ll have been flying non-stop for 15 hours before we get to the destination.</b></p>
