                        <h3>Грамматический нюанс 1</h3>
                        <p>Когда наша уверенность в том, что произойдет в будущем, касается любого расписания (общественного транспорта, театров, графика работы и т. д.), тогда используют время <b>Present Simple</b>.</p>
                        <div class="example-w pb5">
                            <p>Поезд отходит через 5 минут. — <b>The train leaves in 5 minutes.</b></p>
                            <p>В котором часу начинается фильм? — <b>What time does the film begin?</b></p>
                            <p>Завтра понедельник. — <b>Tomorrow is Monday.</b></p>
                            <p>Я начинаю работать в пятницу. — <b>I start on Friday.</b></p>
                        </div>
                        <div class="separately-block6"></div>
                        <p>Так что одинаковые по сути фразы «<i>Поезд отходит через 5 минут</i>» и «<i>Мама уезжает через 5 минут</i>» при переводе на английский язык требуют разных грамматических времен.</p>
                        <div class="example-w pb5">
                            <p>Поезд прибывает через полчаса. — <b>The train arrives in half an hour.</b></p>
                            <p>Мама приезжает через полчаса. — <b>Mum is arriving in half an hour.</b></p>
                        </div>
                    </div>
                </div>

                <div class="separately-block3"></div>

                <div class="bordered-w">
                    <div class="t">
                        <h3>Грамматический нюанс 2</h3>
                        <p>Мы часто задаем вопросы типа «<i>Что ты делаешь сегодня вечером?</i>», «<i>Куда ты завтра идешь?</i>». Естественно, что при переводе подобных вопросов на английский язык мы должны пользоваться временем <b>Present Progressive</b>, так как нас интересует именно то решение, которое наш собеседник принял до начала нашего разговора, то, что уже он запланировал к моменту нашего с ним общения.</p>
