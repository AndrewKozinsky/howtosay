                            <p>Подобными ПЛФ пользуются при переводе прямой речи в косвенную.</p>
                            <div class="example-w">
                                <p>Она сказала: «Я вернусь поздно». — <b>She said: “I'll be back late”.</b></p>
                                <p>Она сказала, что вернется поздно. — <b>She said she would be back late.</b></p>
                            </div>
                        </div>
                    </div>
