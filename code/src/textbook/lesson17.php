                        <p>«Незаконченное время» — это период времени, который еще не закончился, <i>то есть еще длится в данный момент</i>. Обычно обозначается словами:</p>

                        <div class="example-w pb5">
                            <p>Сегодня — <b>today</b></p>
                            <p>На этой неделе — <b>this week</b></p>
                            <p>В этом месяце — <b>this month</b></p>
                            <p>В этом году — <b>this year</b></p>
                            <p>В этом десятилетии — <b>this decade</b></p>
                            <p>В этом столетии — <b>this century</b></p>
                            <p>В этом тысячелетии — <b>this millennium</b></p>
                            <p>В жизни (это словосочетание может и не называться, оно может только подразумеваться).</p>

                            <div class="example-w pb5">
                                <p>Я сегодня ходила в театр (сегодняшний день еще не кончился). — <b>I have gone to the theatre today.</b></p>
