                            <h3>Грамматический нюанс 5</h3>
                            <p>Если после слова «сказал», которое входит в состав главного предложения вышеназванных ПЛФ, называют того, к кому было обращено высказывание, то на английский язык подобное словосочетание можно переводить 2-мя способами:</p>
                            <div class="example-w">
                                <p>Сказал мне (<i>ей, маме</i>) —</p>
                                <div class="example-w">
                                    <ol>
                                        <li>
                                            <p><b>told me</b> (<i>her, mother</i>)</p>
                                        </li>
                                        <li>
                                            <p><b>said to me</b> (<i>her, mother</i>)</p>
                                        </li>
                                    </ol>
                                </div>
                                <div class="separately-block4"></div>
                                <p>Он сказал мне, что вы сейчас работаете над книгой. —</p>
                                <div class="example-w">
                                    <ol>
                                        <li>
                                            <p><b>He told me that you were working on a book then.</b></p>
                                        </li>
                                        <li>
                                            <p><b>He said to me that you were working on a book then.</b></p>
                                        </li>
                                    </ol>
                                </div>
                            </div>
                        </div>
                    </div>
