                                <p>Вы знаете хотя бы приблизительно, сколько это будет стоить? — <b>Do you know how much it will cost at least approximately?</b></p>
                                <p>Я поинтересовалась, сколько это будет стоить. — <b>I wondered how much it would cost.</b></p>
                                <div class="separately-block4"></div>
                                <p>Что вы имеете в виду? — <b>What do you mean?</b></p>
                                <p>Пожалуйста, объясните, что вы имеете в виду. — <b>Explain, please, what you mean.</b></p>
                                <p>Он объяснил, что вы имеете в виду. — <b>He explained what you meant.</b></p>
                                <div class="separately-block4"></div>
