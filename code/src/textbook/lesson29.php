                    <div class="bordered-w">
                        <div class="t">
                            <h3>Грамматический нюанс 7</h3>
                            <p>Если в косвенной речи передается ряд последовательных действий, то первое действие дается в Past Perfect, а последующие в Past Simple.</p>
                            <div class="example-w">
                                <p>Она сказала мне, что была в кино, где встретила своего старого друга. — <b>She told me that she had been </b>(<i>Past Perfect</i>) <b>to the cinema, where she met </b>(<i>Past Simple</i>)<b> an old friend.</b></p>
                            </div>
                        </div>
                    </div>
                    <div class="separately-block3"></div>

                    <div class="bordered-w">
                        <div class="t">
                            <h3>Грамматический нюанс 8</h3>
                            <p>На месте глаголов сообщения в ПЛФ выше могут быть глаголы другой смысловой группы — назовем их глаголами выяснения:</p>
                            <div class="example-w">
                                <p>спрашивать — <b>to ask</b></p>
                                <p>интересоваться — <b>to wonder</b></p>
                                <p>выяснять — <b>to find out</b></p>
                                <p>хотеть узнать — <b>to want to know</b></p>
                                <div class="separately-block6"></div>
                                <p>Он спросил меня, иду ли я в кино (<i>Тут используется правило из второго грамматического нюанса <a href="lesson23.php">23 урока</a></i>). — <b>She asked me if I was going to the cinema.</b></p>
                                <p>Моя мама поинтересовалась, поступил ли мой друг в университет. — <b>My mum wondered if my friend had entered the university.</b></p>
                                <p>Затем они постарались выяснить, собираемся ли мы еще здесь остаться. — <b>Then they tried to find out if we were going to stay there long.</b></p>
                                <p>Он хотел знать, возвратимся ли мы домой засветло. — <b>He wonted to know if we would have came back before dark.</b></p>
                                <p>Он спросил, был ли я сегодня в кино. — <b>He asked if I had been to the cinema that day.</b></p>
                            </div>
                        </div>
                    </div>
                    <div class="separately-block3"></div>

                    <div class="bordered-w">
                        <div class="t">
                            <h3>Грамматический нюанс 9</h3>
                            <p>Если мы хотим что-то выяснить, и при этом начинаем придаточное <b>дополнительное словами</b>:</p>
                            <div class="example-w">
                                <p>Он хотел узнать, <b>что</b> я делаю в свободное время.</p>
                                <p> Он спросил меня, <b>когда</b> она вернется домой.</p>
                                <p>Он поинтересовался, <b>где</b> они живут.</p>
                                <p>Он постарался выяснить, <b>почему</b> она обратилась по поводу работы к его шефу.</p>
                                <p>Он поинтересовался, <b>как долго</b> моя мама работает на своей нынешней работе.</p>
                            </div>
                            <div class="separately-block6"></div>
                            <p>...то придаточное дополнительное звучит, как вопрос. Однако нужно уметь различать придаточные вопросительные и придаточные дополнительные, похожие на вопросительные.</p>
                            <div class="separately-block3"></div>

                            <p>В русском языке они различаются интонацией, а порядок слов не меняется. А в английском различаются и интанацией и порядком слов.</p>
                            <div class="example-w">
                                <ol>
                                    <li>
                                        <p>Когда она вернется домой? — <b>When will she come back home?</b> <i class="gray-text">(Заметьте, что will идет до she чтобы подчеркнуть, что это вопросительное предложение, а не повествовательное как в предложении ниже.)</i></p>
                                    </li>
                                    <li>
                                        <p>Он хочет знать, когда она вернется домой. — <b>He wants to know when she will come back home.</b> <i>(Тут не вопрос, а констатация, что он хочет знать).</i></p>
                                    </li>
                                </ol>
                            </div>
                            <div class="separately-block4"></div>

                            <p>То есть в английском языке в придаточном дополнительном предложении, похожем на вопрос, сохраняется повествовательный порядок слов (вне зависимости от того, в каком времени стоит глагол в главном предложении!).</p>
                            <div class="example-w">
                                <p>Он спросил, когда она вернется домой. — <b>He asked me when she would come back home.</b></p>
                                <p>Который час? — <b>What time is it?</b></p>
                                <p>Ты знаешь, который час. — <b>You know what time it is.</b></p>
                                <p>Ты знаешь, который час? — <b>Do you know what time it is?</b></p>
                                <p>Ты знала, который час. — <b>You knew what time it was.</b></p>
                                <p>Ты знала, который час? — <b>Did you know what time it was?</b></p>
                                <div class="separately-block4"></div>
                                <p>Кто эта женщина? — <b>Who is this woman?</b></p>
                                <p>Я не знаю, кто эта женщина. — <b>I don’t know who this woman is.</b></p>
                                <p>Я не знала, кто эта женщина. — <b>I didn’t know who this woman was.</b></p>
                                <div class="separately-block4"></div>
                                <p>Где я могу найти вашего директора? — <b>Where can I find your headmaster?</b></p>
                                <p>Вы можете сказать мне, где я могу найти вашего директора? — <b>Can you tell me where I can find your headmaster?</b></p>
