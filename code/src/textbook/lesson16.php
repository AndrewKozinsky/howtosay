                <div class="bordered-w">
                    <div class="t">
                        <h3>Грамматический нюанс 1</h3>
                        <p>Название времени <b>Present Perfect</b> на русский язык переводится как «Настоящее завершенное». Но любому английскому глаголу в форме <b>Present Perfect</b> всегда соответствует русский глагол в форме прошедшего времени несовершенного (<i>отвечает на вопрос «Что делал?»</i>) либо совершенного вида (<i>отвечает на вопрос «Что сделал?»</i>).</p>
                        <div class="example-w pb5">
                            <p>Я уже (<i>что делал?</i>) читал эту книгу. — <b>I have already read this book.</b></p>
                            <p>Я только что (<i>что сделал?</i>) прочел эту книгу. — <b>I have just read this book.</b></p>
                        </div>
                    </div>
                </div>

                <div class="separately-block3"></div>

                <p><b>Случаи употребления времени Present Perfect:</b></p>
                <div class="separately-block4"></div>

                <div class="t">
                    <ol>
                        <li>
                            <p>Когда сообщают какую-то новость.</p>
                            <div class="example-w">
                                <p>Президент прибыл в Лондон. — <b>The President has arrived in London.</b></p>
                                <p>Я поранил палец. — <b>I have cut my finger.</b></p>
                            </div>
                            <div class="separately-block4"></div>
                        </li>
                        <li>
                            <p>Когда ситуацию в настоящем объясняют событиями прошлого.</p>
                            <div class="example-w">
                                <p>Он хорошо говорит по-английски, так как три года жил в Англии. — <b>He speaks perfect English as he has lived in England for 3 years.</b></p>
                                <p>Он много знает (<i>почему?</i>). Он много читал. — <b>He knows a lot: he has read a lot.</b></p>
                                <p>Она интересный человек (<i>почему?</i>). Она побывала во многих странах и работала с замечательными людьми. — <b>She is an interesting person. She has been in many countries and has worked with remarkable people.</b></p>
                            </div>
                            <div class="separately-block4"></div>
                        </li>
                        <li>
                            <p>Когда вместе с глаголом в прошедшем времени употребляют слово «<b>уже</b>».</p>
                            <div class="example-w">
                                <p>Я уже прочел книгу. — <b>I have already read the book.</b></p>
                            </div>
                        </li>
                    </ol>
                </div>

                <div class="separately-block3"></div>

                <div class="bordered-w">
                    <div class="t">
                        <h3>Грамматический нюанс 2</h3>
                        <p>Слово <b>already</b> ставят между <b>have/has</b> и <b>глаголом</b>.</p>
                    </div>
                </div>

                <div class="separately-block3"></div>

                <div class="bordered-w">
                    <div class="t">
                        <h3>Грамматический нюанс 3</h3>
                        <p>В вопросительных и отрицательных предложениях слово <b>already</b> меняется на <b>yet</b> и ставится в конце предложения.</p>
                        <div class="example-w">
                            <p>Ты уже прочел эту книгу? — <b>Have you read this book yet?</b></p>
                        </div>
                    </div>
                </div>

                <div class="separately-block3"></div>

                <div class="bordered-w">
                    <div class="t">
                        <h3>Грамматический нюанс 4</h3>
                        <p>Слово <b>yet</b> на русский язык переводится как «<b>еще не</b>».</p>
                        <div class="example-w">
                            <p>Я еще не готова. — <b>I am not ready yet.</b></p>
                            <p>Он еще не прочел книгу. — <b>He has not read a book yet.</b></p>
                        </div>
                    </div>
                </div>

                <div class="separately-block3"></div>

                <div class="bordered-w">
                    <div class="t">
                        <h3>Грамматический нюанс 5</h3>
                        <p>Слово <b>already</b> не меняется в вопросительном предложении на слово <b>yet</b> только в том случае, если оно имеет значение «так скоро», а вопросительное предложение в целом выражает удивление.</p>
                        <div class="example-w">
                            <p>Неужели вы уже прочли книгу? — <b>Have you already read the book?</b></p>
                        </div>
                    </div>
