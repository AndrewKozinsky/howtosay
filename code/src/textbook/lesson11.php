                    <p><b>Слова-показатели:</b> все слова, которые обозначают истекшее (законченное) время:</p>
                    <div class="example-w">
                        <p>вчера — <b>yesterday</b></p>
                        <p>на прошлой неделе — <b>last week</b></p>
                        <p>2 дня назад — <b>2 days ago</b></p>
                        <p>в прошлом году — <b>last year</b></p>
                        <p>в детстве — <b>in childhood</b></p>
                        <p>в юности — <b>in youth</b></p>
                    </div>
                    <div class="separately-block4"></div>

                    <p>Формула времени — <b>V + ed</b> для правильных глаголов и <b>V2</b> для неправильных.</p>
                    <div class="separately-block4"></div>

                    <p>Правильные глаголы — это глаголы, которые образуют утвердительную форму простого прошедшего времени путем прибавления к основе инфинитива суффикса <b>-ed</b>.</p>
                    <div class="example-w">
                        <p>открыл — open<b>ed</b></p>
                        <p>жил — liv<b>ed</b></p>
                        <p>играл — play<b>ed</b></p>
                        <p>работал — work<b>ed</b></p>
                        <p>хотел — want<b>ed</b></p>
                        <p>прыгал — jump<b>ed</b></p>
                        <p>танцевал — danc<b>ed</b></p>
                        <p>старался — tri<b>ed</b></p>
                        <p>прекратился — stopp<b>ed</b></p>
                    </div>
                    <div class="separately-block6"></div>
                    <p>Как видите, суффиксу <b>-л</b> в форме глаголов прошедшего времени в русском языке соответствует суффикс <b>-ed</b> в форме глаголов прошедшего времени в английском языке.</p>
                    <div class="separately-block4"></div>

                    <p>Неправильные глаголы — это такие глаголы, которые образуют форму прошедшего времени путем изменения глагольной основы:</p>

                    <div class="example-w">
                        <p>иду &rarr; ходил — <b>go &rarr; went</b></p>
                        <p>говорю &rarr; сказал — <b>say &rarr; said</b></p>
                        <p>пишу &rarr; написал, писал — <b>write &rarr; wrote</b></p>
                        <p>бегу &rarr; побежал, бежал — <b>run [rʌn] &rarr; ran[ræn]</b></p>
                        <p>несу &rarr; принес — <b>bring &rarr; brought</b></p>
                        <p>понимаю &rarr; понял — <b>understand &rarr; understood</b></p>
                        <p>делаю &rarr; делал, сделал — <b>do &rarr; did</b></p>
                        <p>есть &rarr; был (я, он) — <b>be &rarr; was</b></p>
                        <p>есть &rarr; был (ты), были — <b>be &rarr; were</b></p>
                        <p>имею &rarr; имел — <b>have &rarr; had</b></p>
                    </div>
                    <div class="separately-block4"></div>

                    <p>Форма прошедшего времени неправильных глаголов обычно обозначается <b>v2</b> и ее можно найти в <a href="../wrong-verb.php">таблице неправильных глаголов</a>.</p>
                    <p>Для того чтобы правильно выполнять артикуляционно-грамматические упражнения во всех последующих уроках, необходимо знать, каким является глагол, правильным или неправильным. Поэтому каждый глагол, который вы встретите в упражнениях «Техника речи-1» и «Техника речи-2», надо сверять со <a href="../wrong-verb.php">списком неправильных глаголов</a>. Если глагола в этом списке нет, значит, он правильный. Формы прошедшего времени неправильных глаголов надо учить наизусть.</p>

                    <div class="separately-block3"></div>

                    <p><b>Вопросительная форма</b> глаголов во времени <b>Past Simple</b> образуется при помощи вспомогательного глагола <b>did</b>, который ставится перед подлежащим, а за подлежащим следует смысловой глагол в форме голого инфинитива:</p>

                    <div class="example-w">
                        <p>Ты ходил вчера в кино? — <b>Did you go to the cinema yesterday?</b></p>
                        <p>Ты делал опыты на прошлой неделе? — <b>Did you carry out experiments last week?</b></p>
                        <p>Она работала 5 дней назад? — <b>Did she work 5 days ago?</b></p>
                        <p>Ты играл в теннис в детстве? — <b>Did you play tennis in your childhood? / Did you play tennis as a child?</b></p>
                    </div>
                    <div class="separately-block3"></div>

                    <div class="bordered-w">
                        <div class="t">
                            <p><b>Интонационный нюанс:</b> <i>в вопросительном предложении на последнем ударном слоге тон голоса повышается</i>.</p>
                        </div>
                    </div>


                    <div class="separately-block3"></div>

                    <p><b>Отрицательная форма</b> глаголов во времени <b>Past Simple</b> образуется при помощи вспомогательного глагола <b>did</b> и отрицания <b>not</b>, которые ставятся перед смысловым глаголом в форме голого инфинитива:</p>

                    <div class="example-w">
                        <p>Я не видела его вчера. — <b>I didn’t see him yesterday.</b></p>
                        <p>Она не понимала меня в прошлом году. — <b>She didn’t understand me last year.</b></p>
                        <p>Мы не работали вместе в юности. — <b>We didn’t work together in youth.</b></p>
                        <p>В прошлом месяце ты ничего не заработал. — <b>You earned nothing last year.</b></p>
                    </div>
                </div>

                <div class="separately-block3"></div>

                <div class="advice-w">
                    <div class="advice-block-name">Совет</div>
                    <p>Слово <b>did</b> — это вторая форма неправильного глагола <b>do</b>. А как вы знаете вторая форма указывает на прошедшее время.</p>
                </div>

                <div class="separately-block3"></div>

                <div class="advice-w">
                    <div class="advice-block-name">Совет</div>
                    <p>Иногда вопрос задают с отрицанием. Например «Почему ты никогда не убегал от меня в детстве?». В этом случае к вспомогательному глаголу did прибавляется частица not. А слово never не пишется — <b>Why did not you run away from me in childhood?</b></p>
                </div>

                <div class="separately-block3"></div>

                <div class="advice-w">
                    <div class="advice-block-name">Совет</div>
                    <p>В английском языке почти полностью атрофировались суффиксы и окончания. Если в русском изменяя окончание, приставки и суффиксы можно рассказать о времени и характере действия (я дошел, я ходил, я иду, я пойду), то в английском для этих целей ставятся слова-помощники did, do, does, will, have, has. Если слова-помощники не применяются (например в утвердительном предложении в Past Simple), то чтобы показать глагол в прошедшем времени применяют окончание -ed или ставят во вторую форму.</p>
                </div>












