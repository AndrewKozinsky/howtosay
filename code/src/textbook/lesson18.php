                    <div class="t">
                        <h3>Грамматический нюанс 3</h3>
                        <p>Одно и тоже предложение на английский язык нужно переводить, используя разные английские грамматические времена, в зависимости от того, в каком контексте это предложение употребляется. Например, предложение «Она была в кино».</p>
                        <p><b>Ситуация 1.</b> Она только вошла в дом, когда я позвонил. Она была в кино до того, как я позвонил. Следовательно мы должны использовать время <b>Past Perfect</b>.</p>
                        <div class="example-w pb5">
                            <p><b>She had been to the cinema.</b></p>
                        </div>

                        <p><b>Ситуация 2.</b> Ее не было дома, когда я звонил. Она была в кино именно в тот самый момент, когда я позвонил. По идее, при переводе этого предложения на английский язык мы должны использовать время <b>Past Progressive</b>, но глагол to be в значении «быть, находиться» не употребляется в <b>v-ing</b>, поэтому мы используем время <b>Past Simple</b>.</p>
                        <div class="example-w pb5">
                            <p><b>She was in he cinema.</b></p>
                        </div>
                    </div>
                </div>

