                    <p>Для того чтобы выразить <i>умение</i> что-либо делать в английском языке употребляют модальный глагол <b><i>can</i></b>, за которым следует инфинитив смыслового глагола без <i>to</i>.</p>
                    <p>Инфинитив — это неопределенная форма глагола, которая отвечает на вопрос «Что делать?» В английском языке показателем неопределенной формы глагола является <i>to</i>, стоящее перед глаголом:</p>
