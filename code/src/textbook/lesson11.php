                        <h3>Грамматический нюанс</h3>
                        <p>Глагол <b>to be</b> и в прошедшем времени сохраняет свою самодостаточность, то есть для образования вопросительной и отрицательной форм прошедшего времени глагол <b>to be</b> не нуждается в других вспомогательных глаголах:</p>
                        <div class="separately-block6"></div>
                        <div class="example-w">
                            <p>Я был вчера в театре — <b>I was at the theatre yesterday.</b></p>
                            <div class="separately-block4"></div>
                            <p>Ты был вчера в театре? — <b>Were you at the theatre yesterday?</b></p>
                            <p>Он был вчера в школе? — <b>Was he at school yesterday?</b></p>
                            <p>Вы были на море в прошлом году? — <b>Were you at sea last year?</b></p>
                            <div class="separately-block4"></div>
                            <p>Я вчера не был в театре. — <b>I wasn’t at the theatre yesterday.</b></p>
