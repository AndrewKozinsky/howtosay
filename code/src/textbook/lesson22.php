                    <div class="t">
                        <h3>Грамматический нюанс 4</h3>
                        <p>Существует еще один вспомогательный глагол <b>shall</b>, при помощи которого образуется форма <b>Future Simple</b> для I и we. В отличии от will, shall используется для выражения категорического обещания. Таким образом сообщается об неизбежности события.</p>
                        <div class="example-w pb5">
                            <p>Я пойду завтра в кино. — <b>I shall go to the cinema tomorrow.</b></p>
                            <p>Мы пойдем завтра в кино. — <b>We shall go to the cinema tomorrow.</b></p>
                        </div>
                        <div class="separately-block6"></div>
                        <p>Но в разговорной речи более употребительным является вспомогательный глагол <b>will</b> для всех лиц и чисел.</p>
                    </div>
                </div>

                <div class="separately-block3"></div>

                <div class="bordered-w">
                    <div class="t">
                        <h3>Грамматический нюанс 5</h3>
                        <p>Вспомогательный глагол <b>shall</b> употребляется в английском языке в вопросительных предложениях, когда мы интересуемся чьим-либо мнением по какому-либо вопросу.</p>
                        <div class="example-w pb5">
                            <p>У меня нет денег. Что мне делать? — <b>I have no money. What shall I do?</b></p>
                            <p>Как ты думаешь, куда бы нам пойти сегодня вечером? — <b>Where shall we go tonight?</b></p>
                            <p>Холодно. Закрыть окно? — <b>It’s cold. Shall I shut ( close) the window?</b></p>
                        </div>
                    </div>
                </div>

