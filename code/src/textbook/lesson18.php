                    <div class="t">
                        <h3>Грамматический нюанс 1</h3>
                        <p><b>Past Perfect</b> употребляют когда хотят подчеркнуть, что одно из двух действий предшествовало другому.</p>


                        <div class="example-w pb5">
                            <p>Твой папа был дома, когда ты пришел? — <b>Was your daddy at home when you came?</b> <i>(Past Simple)</i></p>
                            <div class="example-w">
                                <p><i>Ответ 1:</i> Нет, он уже ушел на работу. — <b>No, he wasn’t. He had already gone to work.</b> <i>Past Perfect потому что папа ушел раньше.</i></p>
                                <p><i>Ответ 2:</i> Да, но вскоре после этого он ушел. — <b>Yes, he was. But shortly afterwards he went out.</b> <i>Past Simple потому что папа ушел после.</i></p>
                            </div>
                            <div class="separately-block6"></div>
                            <p>Мы жили в Тбилиси до того, как переехали в Москву. — <b>We had lived in Tbilisi before we moved to Moscow.</b> <i>Первая часть предложения стоит в Past Perfect потому что это действие произошло раньше, чем переезд в Москву.</i></p>
                        </div>
                    </div>
                </div>

                <div class="separately-block3"></div>

                <div class="bordered-w">
                    <div class="t">
                        <h3>Грамматический нюанс 2</h3>
                        <p>При констатации двух или нескольких последовательных действий в прошлом <b>Past Perfect</b> не употребляется.</p>
                            <div class="example-w">
                            <p><b>Мы жили в Тбилиси, а потом решили переехать в Москву</b> — то есть, констатируем последовательность двух действий в прошлом, то тогда нужно пользоваться временем <b>Past Simple</b>.</p>

                            <div class="example-w pb5">
                                <p><b>We lived in Tbilisi and then we decided to move to Moscow.</b></p>
                                <p>Она пришла домой, сняла пальто, вымыла руки и села в кресло. — <b>She came home, took off her coat, washed her hands and sank into the armchair.</b></p>
                            </div>
                        </div>
                    </div>
