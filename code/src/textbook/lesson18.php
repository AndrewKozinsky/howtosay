                                <p><b>Случаи употребления:</b></p>
                                <ol>
                                    <li class="pb4">
                                        <p>Если в предложении применяются слова обозначающие завершенное время: вчера, на прошлой неделе, 2 дня назад, в прошлом году, в детстве.</p>
                                    </li>
                                    <li class="pb4">
                                        <p>Если есть словосочитание just now.</p>
                                    </li>
                                    <li class="pb4">
                                        <p>Когда действия происходят последовательно одно за другим.</p>
                                    </li>
                                    <li class="pb4">
