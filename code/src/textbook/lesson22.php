                    <div class="t">
                        <h3>Грамматический нюанс 6</h3>
                        <p>Время <b>Future Simple</b> очень часто употребляется с таким вводными словами и конструкциями как:</p>
                        <div class="example-w pb5">
                            <p>возможно, вероятно — <b>probably</b></p>
                            <p>я рассчитываю — <b>I expect</b></p>
                            <p>я уверен — <b>I’m sure</b></p>
                            <p>я думаю — <b>I think</b></p>
                            <p>я не думаю — <b>I don’t think</b></p>
                            <p>интересно — <b>I wonder</b></p>
                        </div>
                        <div class="separately-block6"></div>
                        <p><b>Примеры:</b></p>
                        <div class="example-w pb5">
                            <p>Сегодня вечером я, возможно, поздно приду домой. — <b>I’ll probably come home late this evening.</b></p>
                            <p>Сегодня я не видела моего мужа. Я рассчитываю, что он позвонит вечером. — <b>I haven’t seen my husband today. I expect he will phone this evening.</b></p>
                            <p>Не волнуйся из-за экзамена. Я уверен, ты его сдашь. — <b>Don’t worry about the exam. I’m sure you’ll pass.</b></p>
                            <p>Я не думаю, что экзамен будет очень сложный. — <b>I don’t think the exam will be very difficult.</b></p>
                            <p>Интересно, что случится через пару дней. — <b>I wonder what will happen in a couple of days.</b></p>
                        </div>
                    </div>
                </div>

