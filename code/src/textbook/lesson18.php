                                <p><b>Случаи употребления:</b></p>
                                <ol>
                                    <li class="pb4">
                                        <p>Когда указывают момент в прошлом и называют точное время используя предлог «к».</p>
                                    </li>
                                    <li class="pb4">
                                        <p>Когда <i>это</i> действие произошло раньше другого.</p>
                                    </li>
                                </ol>
                            </td>
