                        <h3>Грамматический нюанс 3</h3>
                        <p>Отрицательная форма времени <b>Future Simple</b> используется тогда, когда кто-то или что-то отказывается выполнить какое-то действие:</p>
                        <div class="example-w pb5">
                            <p>Я попытался дать ей совет, но она отказывается слушать. — <b>I’ve tried to advise her but she won’t listen ( she refuses to listen).</b></p>
                            <p>Машина не хочет заводиться. Интересно, что с ней. — <b>The car won’t start (the car “refuses” to start). I wonder what’s wrong with it?</b></p>
                        </div>
                    </div>
                </div>

