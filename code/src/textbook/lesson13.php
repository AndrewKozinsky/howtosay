                    <div class="t">
                        <h3>Грамматический нюанс 1</h3>
                        <p>Конструкция <b>used + инфинитив</b> может употребляться и при использовании особого, непереводимого, на русский язык оборота <b>there is (there are)</b>.</p>
                        <p>Если хотите подчеркнуть наличие кого-нибудь или чего-нибудь в определенном месте, то предложение нужно <i>начать оборотом</i> <b>there is (there are)</b>, за которым должно следовать существительное, обозначающее название этого «кого-нибудь» или «чего-нибудь»:</p>
                        <div class="separately-block6"></div>
                        <div class="example-w">
                            <p>Стул в комнате. — <b>There is a chair in the room.</b></p>
                            <p>Стулья в комнате. — <b>There are chairs in the room.</b></p>
                            <p>Словарь и 2 новые английские книжки — на полке. — <b>There is a dictionary and 2 new English books on the shelf.</b></p>
                        </div>
                        <div class="separately-block6"></div>
                        <p>Форма сказуемого в этом обороте согласуется с первым существительным, следующим за ним.</p>

                        <p>В обороте <b>there is</b> слово <b>there</b> не имеет самостоятельного значения, поэтому при переводе на английский язык русских предложений, в которых обстоятельство места выражено словом ТАМ, необходимо в конце предложения употребить слово <b>there</b>:</p>
                        <div class="example-w">
                            <p>Там было много собак. — <b>There were a lot of dogs there.</b></p>
                        </div>

                        <div class="separately-block3"></div>

                        <p>Вопросительная форма образуется при помощи перестановки:</p>

                        <div class="example-w">
                            <p>Словарь — на полке? — <b>Is there a dictionary on the shelf?</b></p>
                        </div>
                        <div class="separately-block3"></div>

                        <p>Отрицательная форма образуется при помощи отрицательной частицы not:</p>

                        <div class="example-w">
                            <p>Словаря на полке нет. — <b>There isn’t a dictionary on the shelf.</b></p>
                        </div>

                        <div class="separately-block3"></div>

                        <p>Как и любая другая конструкция, в состав которой входит глагол to be в любом из своих обличий (в данном случае — <b>is</b>), оборот <b>there is (there are)</b> может изменяться по временам:</p>

                        <div class="example-w pb5">
                            <p>В прошлом месяце было много хороших дней. — <b>There were many good days last month.</b></p>
                            <p>Раньше в городе был кинотеатр (а теперь — нет). — <b>There used to be a cinema in the town.</b></p>
                            <p>Раньше в городе было 5 кинотеатров, а теперь — один. — <b>There used to be 5 cinemas in the town but there is only one now.</b></p>
                        </div>
                        <div class="separately-block3"></div>

                        <p>Также смотрите <a href="../articles/tobe.php">таблицу изменения глагола to be по временам</a>.</p>
                    </div>
