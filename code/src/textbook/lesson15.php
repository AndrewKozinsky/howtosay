                        <h3>Грамматический нюанс 7</h3>
                        <p>Не надо путать ПЛФ «Я делал это в 5» и ПЛФ «Я сделал это в 5». В ПЛФ «Я делал это в 5» подчеркивается именно то, что кто-то находился в середине действия в определенный момент в прошлом. А в ПЛФ «Я сделал это в 5» лишь констатируется факт, что это произошло и выдается дополнительная информация о том, когда это произошло. Поэтому ПЛФ «Я сделал это в 5» требует английского грамматического времени <b>Past Simple</b>.</p>
                        <div class="example-w pb5">
                            <p>Я обедал вчера в 5. — <b>I was having dinner yesterday at 5.</b></p>
                            <p>Я пообедал вчера в 5. — <b>I had dinner yesterday at 5.</b></p>
