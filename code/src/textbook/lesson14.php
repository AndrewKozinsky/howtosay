                <div class="t">
                    <p>ПЛФ 1 выражает умение что-либо делать в прошлом (в детстве, в юности, много лет назад, давным-давно). Для этого употребляют глагол <b>could</b> (<i>это форма прошедшего времени глагола can</i>), за которым следует голый инфинитив.</p>
                    <div class="example-w">
                        <p>Мой дедушка умел говорить на 5 языках. — <b>My grandpa could speak 5 foreign languages.</b></p>
                        <p>В юности мой папа мог побить кого угодно. — <b>My dad could beat anybody in youth </b>[juːθ]<b>.</b></p>
                    </div>

                    <div class="separately-block3"></div>

                    <p>В вопросительной форме глагол <b>could</b> ставиться перед подлежащим.</p>
                    <div class="example-w">
                        <p>Твой дедушка умел говорить на 5 языках в молодости? — <b>Could your grandpa speak 5 foreign languages in youth?</b></p>
                        <p>Твой брат умел плавать в детстве? — <b>Could your brother swim in childhood?</b></p>
                        <p>Ты умел говорить по-английски 2 недели тому назад? — <b>Could you speak English 2 weeks ago?</b></p>
                    </div>

                    <div class="separately-block3"></div>

                    <p>Отрицательная форма образуется путем прибавления к <b>could</b> отрицательной частицы <b>not</b>.</p>
                    <div class="example-w">
                        <p>Я не умел говорить по-английски 2 недели тому назад. — <b>I couldn’t </b>[ˈkudnt]<b> speak English 2 weeks ago.</b></p>
                        <p>Мой брат не умел плавать в детстве. — <b>My brother couldn’t swim in childhood.</b></p>
                    </div>
                    <div class="separately-block3"></div>

                    <p>Итак, ПЛФ «Я умел делать это» выражает обычную способность человека что-то делать в прошлом. Но если хотят подчеркнуть, что человек СМОГ что-то сделать в определенной ситуации, используют ПЛФ «Я сумел сделать это». Ей соответствуют 2 конструкции:</p>
                    <div class="example-w">
                        <ol>
                            <li>
                                <p><b>was \ were able to</b></p>
                            </li>
                            <li>
                                <p><b>managed to</b></p>
                            </li>
                        </ol>
                        <div class="separately-block6"></div>
                        <p>Мне вчера удалось поговорить с иностранцем. — <b>Yesterday I managed to speak to a foreigner </b>[ˈfɔːrənər]<b>.</b></p>
                        <p>Огонь распространился по зданию очень быстро, но всем удалось убежать. — <b>The fire spread through the building quickly but everybody was able to escape.</b></p>
                        <p>Вначале они не хотели идти с нами, но потом нам удалось их уговорить. — <b>They didn’t want to come with us at first but we managed to persuade them.</b></p>
                        <p>Вчера мой папа и мой дядя играли в теннис. Мой дядя играл очень хорошо, но, в конце концов, моему папе удалось побить моего дядю, хотя в юности мой дядя мог побить кого угодно, ведь он великолепно играл в теннис. — <b>My dad and my uncle had a game of tennis yesterday. My uncle played very well but in the end my dad managed to beat him, though my uncle could beat anybody in youth, as he was good at playing tennis.</b></p>
                    </div>

                    <div class="separately-block3"></div>

                    <p>Вопросительная форма ПЛФ 2:</p>
                    <div class="example-w">
                        <p>Тебе удалось вчера поговорить с иностранцем?</p>
                        <div class="example-w">
                            <ol>
                                <li>
                                    <p><b>Did you manage to speak to a foreigner yesterday?</b></p>
                                </li>
                                <li>
                                    <p><b>Were you able to speak to a foreigner yesterday?</b></p>
                                </li>
                            </ol>
                        </div>
                    </div>

                    <div class="separately-block3"></div>

                    <p>Отрицательной форме ПЛФ 2 («Я не смог сделать это» или «Мне не удалось сделать это») в английском языке соответствуют 3 конструкции:</p>
                    <div class="example-w">
                        <p>Мне не удалось вчера поговорить с иностранцем.</p>
                        <div class="example-w">
                            <ol>
                                <li>
                                    <p>I <b>didn’t manage to</b> speak to a foreigner yesterday.</p>
                                </li>
                                <li>
                                    <p>I <b>wasn’t able to</b> speak to a foreigner yesterday.</p>
                                </li>
                                <li>
                                    <p>I <b>couldn’t</b> speak to a foreigner yesterday.</p>
                                </li>
                            </ol>
                        </div>
                    </div>
                </div>





