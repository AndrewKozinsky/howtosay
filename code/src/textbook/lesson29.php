                    <div class="bordered-w">
                        <div class="t">
                            <h3>Грамматический нюанс 6</h3>
                            <p>На месте глагола «сказал» в ПЛФ подобного типа могут быть и другие глаголы — назовем их глаголами сообщения:</p>
                            <div class="example-w">
                                <p>отвечать — <b>to answer</b></p>
                                <p>сообщать — <b>to report</b></p>
                                <p>информировать — <b>to inform</b></p>
                                <p>объявлять — <b>to declare, to announce</b></p>
                                <p>думать — <b>to think</b></p>
                                <p>предупреждать — <b>to warn</b></p>
                                <p>объяснять — <b>to explain</b></p>
                                <p>обещать — <b>to promise</b></p>
                                <p>писать — <b>to write</b></p>
                                <div class="separately-block6"></div>
                                <p>Он ответил, что у него нет при себе денег. — <b>He answered he was out of cash.</b></p>
                                <p>Они сообщили, что приедут к началу учебного года. — <b>They reported that they would have come by the beginning of the school year.</b></p>
                                <p>Она объявила, что на следующей неделе выходит замуж. — <b>She declared that she would marry the next week.</b></p>
                                <p>Она подумала, что он обрадуется ее приезду. — <b>She thought he would be glad to her coming.</b></p>
                                <p>Он предупредил, что вернется нескоро. — <b>He warned he wouldn't be (come) back soon.</b></p>
                                <p>Мы им объяснили, что ничего не имели против них. — <b>We explained to them that we had nothing against them.</b></p>
                                <p>Они пообещали нам, что вернут деньги вовремя. – <b>They promised they would give the money back on time.</b></p>
                                <p>Он написал маме, что у него все хорошо. – <b>He wrote to his mum, that he was fine.</b></p>
                            </div>
