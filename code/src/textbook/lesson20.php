                        <div class="separately-block6"></div>
                        <p>Неважно, забор уже полностью покрашен или только частично. Главное, что ситуацию в настоящем (<i>грязную одежду и руки</i>) объясняют действием, которое к ней привело.</p>
                        <p>Когда спрашивают что привело к тому, что дело в настоящем обстоит именно так, тоже, естественно, используют Present Perfect Progressive.</p>
                        <div class="example-w pb5">
                            <p>Ты такой грязный! Что ты делал? — <b>You are so dirty! What have you been doing?</b></p>
