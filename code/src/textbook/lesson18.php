                <div class="t">
                    <p><b>Дух времени</b> — наличие результата к какому-то моменту в прошлом.</p>
                    <p><b>Формула времени</b> — <b>had + v3</b></p>
                    <p>Момент в прошлом, к которому действие уже произошло, может выражаться двумя способами:</p>
                    <div class="example-w">
                        <ol>
                            <li>
                                <p>Указанием конкретного времени с предлогом «к» — <b>by</b>. «К 5 часам», «к понедельнику», «к началу уроков».</p>
                                <div class="example-w">
                                    <p>Я прочел эту книгу к 5 часам. — <b>I had read this book by 5 o’clock.</b></p>
                                    <p>Он сдал свою работу к понедельнику. — <b>He had handed his home task by Monday.</b></p>
                                    <p>Мы подъехали к школе к началу уроков. — <b>We had driven up to school by the beginning of the lessons.</b></p>
                                </div>
                                <div class="separately-block4"></div>
                            </li>
                            <li>
                                <p>Целым предложением, в котором действие в прошлом выражается формой <b>Past Simple</b>.</p>
                                <div class="example-w">
                                    <p>Я приготовил обед до того, <i>как она пришла</i>. — <b>I had made dinner before <i>she came</i>.</b></p>
                                    <p>Мы покинули город до того, <i>как началась буря</i>. — <b>We had left the town before <i>the storm began</i>.</b></p>
                                </div>
                            </li>
                        </ol>
                    </div>
                    <div class="separately-block3"></div>
                    <p>Вопросительная форма <b>Past Perfect</b> образуется путем постановки глагола <b>had</b> перед подлежащим, а смыслового глагола за подлежащим.</p>
                    <div class="example-w">
                        <p>Он позавтракал до того, как ушел на работу? — <b>Had he had breakfast before he left home for work?</b></p>
                        <p>Она приехала к началу обеда? — <b>Had she arrived by the beginning of the dinner?</b></p>
                    </div>
                    <div class="separately-block3"></div>
                    <p>Отрицательная форма образуется путем постановки после глагола <b>had</b> отрицания <b>not</b>, которое произносится с ударением.</p>
                    <div class="example-w">
                        <p>Мы не закончили нашу работу к 16 июня. – <b>We had not finished our work by the 16-th of June.</b></p>
                        <p>Он не поговорил с директором до своего отъезда в Париж. – <b>He had not spoken to the principal before he left for Paris.</b></p>
                    </div>
                </div>

