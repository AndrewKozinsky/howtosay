                    <p>Существует два вида залога:</p>
                    <div class="example-w">
                        <ol>
                            <li>
                                <p>Действительный – английское название этой грамматической категории: <b>Active voice</b></p>
                            </li>
                            <li>
                                <p>Страдательный – <b>Passive voice</b></p>
                            </li>
                        </ol>
                    </div>
                    <div class="separately-block6"></div>
                    <p><i>Действительный</i> залог используется для того, чтобы описать, <i>что делает</i> подлежащее. <i>Страдательный</i> залог используется для того, чтоб описать, <i>что происходит</i> с подлежащим. Другими словами, подлежащим в <b>Active voice</b> является активный персонаж, который сам совершает действие.</p>
                    <div class="example-w">
