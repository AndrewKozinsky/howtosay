                        <p>Виды артиклей <b><i>a</i></b>(<b><i>an</i></b>) и <b><i>the</i></b>.</p>
                        <p>Артикль <b><i>a</i></b> называется неопределенным и является показателем того, что перед нами <i><b>исчисляемое существительное</b> (поддающееся счету) <b>в единственном числе</b></i>: студент — a student, книга — a book.</p>
                        <p>Артикль <b><i>the</i></b> называется определенным.</p>
                        <p>Итак, «Я — студент» по-английски будет: <b>I am a student.</b></p>
