                <div class="t">
                    <p><b>Дух времени</b> — нахождение в середине действия в течение некоторого времени до определенного момента в прошлом. Причем длительность этого отрезка времени можно не указывать:</p>
                    <div class="example-w">
                        <p>Я делал это до того, как он вошел в комнату.</p>
                    </div>
                    <div class="separately-block6"></div>
                    <p>А можно и указать:</p>
                    <div class="example-w">
                        <p>Я делал это 20 минут до того, как он вошел в комнату.</p>
                    </div>
                    <div class="separately-block6"></div>
                    <p>Момент в прошлом обычно обозначается целым предложением или даже несколькими предложениями, в которых глагол употребляется во времени <b>Past Simple</b>.</p>

                    <div class="separately-block3"></div>

                    <p><b>Формула времени:</b> had been + v-ing</p>
                    <p>Если дают дополнительную информацию о длительности отрезка времени в прошлом, то при переводе используют предлог <b>for</b>.</p>
                    <div class="example-w">
                        <p>Мой папа бросил курить 2 года назад. Он курил 25 лет (<i>то есть находился в середине действия до определенного момента в прошлом, который наступил 2 года назад</i>). — <b>My dad gave up smoking 2 years ago. He had been smoking for 25 years.</b></p>
                        <p>Я был очень уставший, когда пришел домой. Я очень напряженно работал весь день (<i>то есть находился в середине действия до того момента, когда пришел домой</i>). — <b>I was very tired when I came home. I had been working hard all day long.</b></p>
                    </div>
                </div>

