                        <h3>Грамматический нюанс 1</h3>
                        <p>Союз «когда» присоединяет к главному предложению не только придаточные времени, но и придаточные дополнения. А в придаточных дополнениях используется глагол в форме будущего времени. Как в русском языке.</p>

                        <div class="separately-block4"></div>
                        <p>Предложение с придаточным времени. Поэтому придаточное предложение в настоящем времени.</p>
                        <div class="example-w">
                            <p>Я расскажу тебе всё <b>(когда?)</b>, когда ты придешь ко мне в гости. — <b>I’ll tell you everything when you visit me.</b></p>
                        </div>

                        <div class="separately-block4"></div>
                        <p>Предложение с придаточным дополнением. Поэтому придаточное предложение в будущем времени.</p>
                        <div class="example-w">
                            <p>Я постараюсь выяснить <b>(что?)</b>, когда ты придешь ко мне в гости. — <b>I’ll try to find out when you will visit me.</b></p>
                        </div>
                    </div>
                </div>

                <div class="separately-block3"></div>

                <div class="bordered-w">
                    <div class="t">
                        <h3>Грамматический нюанс 2</h3>
                        <p>Разделительный союз if может быть в значении «если» и «ли». Если стоит в значении «если», то следом идет придаточное условия. Поэтому придаточное ставится в настоящем времени.</p>
                        <div class="separately-block6"></div>
                        <p>Если if стоит в значении «ли», то второе предложение становится придаточным дополнительным (отвечает на вопрос «Что?») и остается в будущем временем.</p>
                        <div class="example-w">
                            <p>Я хочу узнать <b>(что?)</b>, придешь ли ты ко мне в гости. — <b>I want to know if you will visit me.</b></p>
                        </div>
                        <div class="separately-block6"></div>
                        <p>Синонимом if служит союз whether [ˈwɛðə], который переводится тоже, как «ли».</p>
                        <div class="example-w">
                            <p>Он спрашивает <b>(что?)</b>, придешь ли ты в воскресенье. — <b>He asks whether you will come on Sunday.</b></p>
                        </div>
                    </div>
                </div>

                <div class="separately-block3"></div>

                <div class="bordered-w">
                    <div class="t">
                        <h3>Грамматический нюанс 3</h3>
                        <p>В английском языке в придаточных времени после союзов:</p>
                        <div class="example-w">
                            <p>когда — <b>when</b></p>
                            <p>после — <b>after</b></p>
                            <p>до тех пор ..., пока не — <b>until</b></p>
                            <p>как только — <b>as soon as</b></p>
                        </div>
                        <div class="separately-block6"></div>
                        <p>можно использовать не только <b>Present Simple</b>, но и <b>Present Perfect</b>, но только если одно действие предшествует другому.</p>
                        <div class="example-w">
                            <p>Когда я позвоню маме, мы сядем обедать (<i>сначала я позвоню маме, а уж потом мы сядем обедать, то есть после разговора по телефону</i>). — <b>When I have phoned my mum, we’ll sit down to dinner.</b></p>
                            <p>Ты можешь одолжить мне книгу, когда ты ее закончишь? — <b>Can you lend me the book when you have finished it?</b></p>
                            <p>Я не дам тебе мороженое до тех пор, пока ты не съешь весь суп. — <b>I won’t give you an ice cream until you have finished your soup.</b></p>
                            <p>Он приедет домой, как только закончит свои исследования. — <b>He will come home as soon as he has finished his research.</b></p>
                            <p>Ты почувствуешь себя лучше, после того как примешь это лекарство. — <b>You’ll feel better after you have taken this medicine.</b></p>
                        </div>

                        <div class="separately-block4"></div>
                        <p>Но если 2 действия в будущем будут происходить одновременно, то тогда в придаточном времени употребляют <b>Present Simple</b>.</p>
                        <div class="example-w">
                            <p>Когда я позвоню маме, я приглашу ее к нам на обед (<i>я буду приглашать ее к нам на обед именно в момент разговора по телефону</i>). — <b>When I phone my mum, I’ll invite her to dinner.</b></p>
                        </div>
                    </div>
                </div>

                <div class="separately-block3"></div>

                <div class="bordered-w">
                    <div class="t">
                        <h3>Грамматический нюанс 4</h3>
                        <p>Несмотря на то, что придаточное предложение «на случай если он придет» является не придаточным условия, а придаточным цели:</p>
                        <div class="example-w">
                            <p>Я сделаю это <b>(для чего? с какой целью?)</b> на случай если он придет.</p>
                        </div>
                        <div class="separately-block6"></div>
                        <p>...в английском варианте этой ПЛФ тоже используется настоящее время (<b>Present Simple</b>) вместо будущего.</p>
                        <div class="example-w pb5">
                            <p>Мы приготовим что-нибудь вкусненькое, на случай если придут гости. — <b>We’ll make something delicious in case guests come.</b></p>
                            <p>Футбольные судьи обычно носят двое часов, на случай если одни из них остановятся во время матча. — <b>Football referees usually wear two watches in case one of them stops during a game.</b></p>
                        </div>
                    </div>
                </div>

                <div class="separately-block3"></div>

                <div class="t">
                    <p><b>Содержание главы вкратце.</b> В английском языке есть сложноподчиненные предложения. Они состоят из главного и придаточного предложения.</p>
                    <p>Придаточное предложение может быть придаточным времени (когда?) или условия (при каком условии?). Тогда оно строится по правилам времени <b>Present Simple</b>.</p>
                    <p>В придаточных времени после союзов «когда, «после», «до тех пор», «пока не», «как только» можно использовать <b>Present Perfect</b>, но только если одно действие предшествует другому.</p>
                    <p>Придаточное дополнения (что?) строится по правилам времени <b>Future Simple</b>.</p>
