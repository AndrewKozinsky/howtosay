                        <p>Этому словосочетанию в английском языке соответствуют два разных слова. <b>lately</b> — «последние дни или недели», <b>recently</b> — последние месяцы или года.</p>
                        <div class="example-w">
                            <p>В последнее время он много пил (последние несколько лет). – <b>He has drunk a lot recently.</b></p>
                            <p>В последнее время она очень изменилась (за последние несколько дней). – <b>She has changed a lot lately.</b></p>
