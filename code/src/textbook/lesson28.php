                        <p>Как долго вы будете лететь прежде, чем вы доберетесь до места назначения? — <b>How long will you have been flying before you get to the destination?</b></p>
                        <p>Мы не будем лететь безостановочно 15 часов прежде, чем долетим до места назначения. — <b>We won’t have been flying non-stop for 15 hours before we get to the destination.</b></p>
                    </div>
