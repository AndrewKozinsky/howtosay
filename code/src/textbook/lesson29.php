                    <p>Как было изучено в <a href="lesson25.php">25 уроке</a> сложноподчиненные предложения состоят из главного и придаточного предложения. Если глагол в главном предложении находится в будущем времени, а придаточное является придаточным времени (<i>отвечает на вопрос «когда?»</i>) или условия (<i>отвечает на вопрос «при каком условии?»</i>), то глагол в придаточном нужно заменять формой настоящего времени.</p>
