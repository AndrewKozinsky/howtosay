                    <div class="separately-block3"></div>

                    <p>Глагол <b><i>can</i></b> в английском языке употребляют не только для того чтобы выразить чье-либо умение что-нибудь делать, но и для того чтобы выразить физическую или умственную способность что-либо сделать:</p>

                    <div class="example-w">
                        <p>Я умею танцевать (вообще) — <b>I can dance</b></p>
                        <p>Я могу станцевать (сейчас) — <b>I can dance now</b></p>
                    </div>

                    <div class="separately-block3"></div>

                    <p>Глагол <b><i>can</i></b> самодостаточен точно так же, как и глагол to be. Для образования отрицательной и вопросительной форм ему не нужны вспомогательные глаголы.</b></p>

                    <div class="separately-block3"></div>

                    <p><b>Отрицательная форма</b> образуется путем прибавления к <b><i>can</i></b> отрицательной частицы <b><i>not</b></i>, которая пишется с ним слитно — <b><i>cannot</b></i>. Сокращенная форма — <b><i>can't</b></i>.</p>

                    <div class="example-w">
                        <p>Он не умеет танцевать — <b>He cannot dance.</b></p>
                    </div>

                    <div class="separately-block3"></div>

                    <p><b>В вопросительной форме</b> глагол <b><i>can</i></b> ставится перед подлежащим:</p>

                    <div class="example-w">
                        <p>Ты умеешь танцевать? — <b>Can you dance?</b></p>
