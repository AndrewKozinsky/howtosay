                        <p><b>Past Progressive</b> употребляется и для выражения параллельности действий в прошлом когда несколько человек одновременно находились в середине своего действия.</p>
                        <p>В русском языке параллельность действий выражается союзом «в то время как» или «пока». Параллельность действий в английском языке выражается словами <b>while</b> и <b>as</b>.</p>
                        <div class="example-w pb5">
                            <p>Я смотрел телевизор, в то время как мама готовила обед. — <b>I was watching TV while mummy was cooking dinner.</b></p>
