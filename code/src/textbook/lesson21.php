                    <div class="t">
                        <h3>Грамматический нюанс 1</h3>
                        <p>При переводе важно не путать <b>Past Progressive</b> и <b>Past Perfect Progressive</b>.</p>
                        <p><b>Past Progressive</b> — это нахождение в середине действия в определенный момент в прошлом.</p>
                        <div class="example-w pb5">
                            <p>Дождь шел, когда мы вышли. — <b>It was raining when we went out.</b></p>
                        </div>
                        <div class="separately-block6"></div>
                        <p><b>Past Perfect Progressive</b> — это нахождение в середине действия <i>до</i> определенного момента в прошлом.</p>
                        <div class="example-w pb5">
                            <p>Светило солнце, но земля была мокрая. По-видимому, дождь шел. — <b>The sun was shining but the ground was wet. To all appearances it had been raining.</b></p>
                        </div>
                        <div class="separately-block6"></div>
                        <p>Как видите, одно и тоже простое предложение («дождь шел»), сказанное по-русски, по-английски будет звучать по-разному, в зависимости от ситуации.</p>
                    </div>
